"""Generate the patch-only T2 evaluation split without changing the T2 generator.

The split uses a seed distinct from the pre-registered T2-related seeds.  It
records the OOD length-rescaling grid for the later patching evaluator; goal
geometry itself remains generated by the unmodified T2 generator.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any

REPO = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO / "src"))

import dgcc.tasks.t2 as t2
from dgcc.tasks.domain import P1_LENGTH_M

# 20260722 is unused by the T2 master seed (20260703), random-target seed
# (20260718), matched-dimension projection seed (20260719), and sprint split
# seed (20260716).  Only this module swaps the seed; t2.py remains unchanged.
PATCH_EVAL_MASTER_SEED = 20260722
PATCH_EVAL_SPLIT_FILENAME = "t2_patch_eval_v1.json"
PATCH_EVAL_VERSION = "t2-patch-eval-v1"

# T2 training fixes P1_LENGTH_M = 1.0 m.  Both values lie outside that singleton
# training-domain boundary and provide shorter/longer OOD directions.  The
# componentwise ratios are consumed by the patch donor rescale mask: xy DCT
# modes 1--7 scale, while modes x0/y0/z0 and all z modes remain absolute.
OOD_LENGTHS_M = (0.75, 1.25)


def parameter_key(spec: dict[str, Any]) -> tuple[Any, ...]:
    """Return the family/parameter/anchor identity used for split deduplication."""
    return (
        spec["family"],
        tuple(sorted((k, round(float(v), 12)) for k, v in spec["params"].items())),
        tuple(round(float(value), 12) for value in spec["anchor"]),
    )


def _generate_seeded_payload() -> dict[str, Any]:
    original_seed = t2.T2_MASTER_SEED
    t2.T2_MASTER_SEED = PATCH_EVAL_MASTER_SEED
    try:
        return t2.generate_t2_payload()
    finally:
        t2.T2_MASTER_SEED = original_seed


def build_patch_eval_payload() -> dict[str, Any]:
    """Build the deterministic patch-only payload and enforce all exclusions."""
    assert PATCH_EVAL_MASTER_SEED not in {20260703, 20260716, 20260718, 20260719}
    generated = _generate_seeded_payload()
    assert generated["master_seed"] == PATCH_EVAL_MASTER_SEED
    heldout_ids = set(generated["splits"]["heldout"])
    specs = [spec for spec in generated["specs"] if spec["goal_id"] in heldout_ids]
    assert len(specs) == 100

    # These committed files are accessed exclusively for the contractually
    # allowed generation-time parameter-level deduplication check.
    t2_v1 = t2.load_t2_payload()
    sprint_path = REPO / "src" / "dgcc" / "tasks" / "splits" / "t2_sprint_heldout_v1.json"
    sprint = json.loads(sprint_path.read_text(encoding="utf-8"))
    candidate_keys = {parameter_key(spec) for spec in specs}
    t2_v1_keys = {parameter_key(spec) for spec in t2_v1["specs"]}
    sprint_keys = {parameter_key(spec) for spec in sprint["specs"]}
    m4_specs = [
        spec for spec in t2_v1["specs"]
        if spec["goal_id"] in set(t2_v1["splits"]["heldout"])
    ]
    m4_keys = {parameter_key(spec) for spec in m4_specs}
    overlap = {
        "t2_v1_all_650": len(candidate_keys & t2_v1_keys),
        "t2_sprint_heldout_v1": len(candidate_keys & sprint_keys),
        "m4_heldout_100": len(candidate_keys & m4_keys),
    }
    if any(overlap.values()):
        raise RuntimeError(f"parameter-level split overlap: {overlap}")

    per_family: dict[str, int] = {}
    for spec in specs:
        per_family[spec["family"]] = per_family.get(spec["family"], 0) + 1
    ratios = [length / P1_LENGTH_M for length in OOD_LENGTHS_M]
    return {
        "version": PATCH_EVAL_VERSION,
        "master_seed": PATCH_EVAL_MASTER_SEED,
        "generator_version": generated["version"],
        "families": generated["families"],
        "n_goals": len(specs),
        "goal_ids": sorted(spec["goal_id"] for spec in specs),
        "specs": specs,
        "family_distribution": per_family,
        "dedup_check_access": {
            "purpose": "dedup_check",
            "sources": ["t2_v1_all_650", "t2_sprint_heldout_v1", "m4_heldout_100"],
            "parameter_key": "family+params+anchor (float values rounded to 12 decimals)",
            "overlap_counts": overlap,
        },
        "ood_length_transform": {
            "l_train_m": P1_LENGTH_M,
            "l_ood_m": list(OOD_LENGTHS_M),
            "l_ood_over_l_train": ratios,
            "training_length_boundary_m": [P1_LENGTH_M, P1_LENGTH_M],
            "ood_direction": "shorter_and_longer_than_fixed_training_length",
            "rescale_mask": {
                "scaled": "x/y DCT modes 1-7",
                "absolute": "x0, y0, z0, and z DCT modes 1-7",
            },
        },
    }


def main() -> int:
    payload = build_patch_eval_payload()
    out = REPO / "src" / "dgcc" / "tasks" / "splits" / PATCH_EVAL_SPLIT_FILENAME
    out.write_text(json.dumps(payload, indent=1, sort_keys=True) + "\n", encoding="utf-8")

    transform = payload["ood_length_transform"]
    report = REPO / "outputs" / "reports" / "patch_eval_gen.md"
    report.parent.mkdir(parents=True, exist_ok=True)
    report.write_text(
        f"""# Patch-only evaluation split generation report

- Generator: `dgcc.tasks.t2` unmodified; seed swap only.
- Seed: **{PATCH_EVAL_MASTER_SEED}**. It differs from T2 master 20260703, sprint held-out 20260716, random-target 20260718, and matched-dimension projection 20260719.
- Goals: {payload['n_goals']} · family distribution: `{json.dumps(payload['family_distribution'], sort_keys=True)}`.
- OOD length metadata: training config `P1_LENGTH_M={P1_LENGTH_M:.2f} m` is a fixed singleton range `[{P1_LENGTH_M:.2f}, {P1_LENGTH_M:.2f}]`; patch evaluation uses `{transform['l_ood_m']} m`, ratios `L_ood/L_train={transform['l_ood_over_l_train']}`. Both values are outside the generator/domain configuration's training boundary, in shorter and longer directions. The donor mask scales x/y DCT modes 1--7 only; x0/y0/z0 and all z modes remain absolute.
- Parameter-level deduplication (purpose: `dedup_check`, key: family+params+anchor rounded to 12 decimals): t2_v1 all 650 = **0**, t2_sprint_heldout_v1 100 = **0**, M4 heldout 100 = **0**.
- File: `src/dgcc/tasks/splits/{PATCH_EVAL_SPLIT_FILENAME}`.
- Preflight stability measurement is deferred: after GPU approval, G10 takes precedence.
""",
        encoding="utf-8",
    )
    print(f"patch eval split: {out} ({payload['n_goals']} goals, overlap=0)")
    print(f"report: {report}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
